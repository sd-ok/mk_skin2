/* =============================================================
   MK_SKIN2 — Footer JavaScript
   [Makeshop 입력 안내] 하단 JS탭에 붙여넣기.
   ============================================================= */

jQuery(document).ready(function ($) {

  /* ============================================================
     모바일 사업자정보 아코디언
     ============================================================ */
  $('#btnBizInfo').on('click', function () {
    var $btn  = $(this);
    var $body = $('#bizInfoBody');

    $btn.toggleClass('is-open');
    $body.stop(true).slideToggle(200);
  });

  /* ============================================================
     스크롤 탑 버튼
     ============================================================ */
  $('.sc_top').on('click', function (e) {
    e.preventDefault();
    $('html, body').animate({ scrollTop: 0 }, 600, 'swing');
  });

});
